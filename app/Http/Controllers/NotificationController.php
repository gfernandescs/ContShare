     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($id_user)
    {
        $id_file = File::select('id')->where('id_user', $id_user)->get()->last();

        $notification = new Notification;

        $notification->user_id = $id_user;
        $notification->file_id = $id_file->id;

        $notification->save();

    }

